import xyz.cofe.lang2.vm.op.Function;

/**
 *
 * @author nt.gocha@gmail.com
 */
public class PythMethod extends Method
{
    public PythMethod(){
    }
    
    public PythMethod(Map owner,Function function){
        super(owner, function);
    }
    
    @Override
    public Object call(Object... arguments) {
        Object[] args = new Object[arguments.length+1];
        System.arraycopy(arguments, 0, args, 1, arguments.length);
        args[0] = getOwner();
        return super.call(args);
    }

    @Override
    public Value deepClone() {
        return super.deepClone(); //To change body of generated methods, choose Tools | Templates.
    }
}
